const express=require('express')
const app = express()
require('dotenv').config()
const cors=require('cors')
const bodyParser=require('body-parser')
const connectDb=require('./db/conection')
const userRouter=require('./Routes/adminrouter')
const empRouter = require('./Routes/emprouter')
const swaggerUI = require('swagger-ui-express')
const options =require('./swaggerdocs')
const swaggerJsDoc = require('swagger-jsdoc')
app.use(bodyParser.json())
app.use(bodyParser.urlencoded({extended:false}))
app.use(express.json())
app.use(cors())
const swaggerDoc=swaggerJsDoc(options)
app.use('/swaggerapi',swaggerUI.serve,swaggerUI.setup(swaggerDoc))
app.use(userRouter)
app.use(empRouter)
app.listen(8000,()=>{console.log(`Server Connected on Port ${process.env.PORT}`)})